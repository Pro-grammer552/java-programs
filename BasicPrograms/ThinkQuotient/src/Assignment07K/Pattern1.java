package Assignment07K;

public class Pattern1 {

	public static void main(String[] args) {
		int n=5;
		for(int i=1;i<=n;i++)
		{
			for(int j=1;j<=i;j++)
			{
				if (i == 0 || j == 0 || i == n - 1
	                    || j == n - 1) 
				System.out.print(" * ");
				
			}
			System.out.println();
		}
		
		
		
		
		
	}
		
		

	}




