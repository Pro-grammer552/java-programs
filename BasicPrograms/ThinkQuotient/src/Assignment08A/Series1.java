package Assignment08A;

public class Series1 {

	public static void main(String[] args) {
		  int s = 2;

	    
	        System.out.println("series :");
	        for (int i = 2; i <= 10; i++) {
	            s = s + (i * i);
	            
	            System.out.print(s + ", ");
	            
	        }
	}

}

